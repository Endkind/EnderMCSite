    <h2 class="text-center my-3">TNT Tag</h2>

    <div class="flex flex-col gap-y-4">
        <div>
            <x-copy.ankor id="what-is-it">
                <h4 class="font-semibold">Was ist TNT Tag?</h4>
            </x-copy.ankor>

            <p>
                TNT Tag ist ein aufregender und actiongeladener Minigame-Modus, in dem Spieler gegeneinander antreten, um nicht der Spieler mit dem TNT zu sein, wenn die Zeit abläuft. Das Spiel endet, wenn nur noch ein Spieler übrig bleibt.
            </p>
        </div>

        <div>
            <x-copy.ankor id="how-to-play">
                <h4 class="font-semibold">So spielst du TNT Tag</h4>
            </x-copy.ankor>

            <ol>
                <li>
                    <strong>Spielstart:</strong> Zu Beginn des Spiels wird ein Spieler zufällig ausgewählt und erhält das TNT.
                </li>
                <li>
                    <strong>Weitergabe des TNT:</strong> Wenn du das TNT hast, musst du es an andere Spieler weitergeben, indem du sie schlägst.
                </li>
                <li>
                    <strong>Zeitlimit:</strong> Jeder Spieler hat eine Minute Zeit, das TNT weiterzugeben, bevor es explodiert.
                </li>
                <li>
                    <strong>Überleben:</strong> Das Ziel ist es, so lange wie möglich zu überleben und nicht das TNT zu tragen, wenn es explodiert.
                </li>
                <li>
                    <strong>Runden:</strong> Das Spiel geht in Runden weiter, bis nur noch ein Spieler übrig ist.
                </li>
            </ol>
        </div>

        <div>
            <x-copy.ankor id="maps">
                <h4 class="font-semibold">Karten</h4>
            </x-copy.ankor>

            <p>
                Wir bieten sieben verschiedene Karten an, auf denen du TNT Tag spielen kannst. Jede Karte hat ihre eigenen einzigartigen Herausforderungen und Verstecke.
            </p>
        </div>

        <div>
            <x-copy.ankor id="tips-and-tricks">
                <h4 class="font-semibold">Tipps und Tricks</h4>
            </x-copy.ankor>

            <ul>
                <li>
                    <strong>Bleib in Bewegung:</strong> Bleibe ständig in Bewegung, um es anderen Spielern schwerer zu machen, dich zu schlagen.
                </li>
                <li>
                    <strong>Verstecke dich:</strong> Nutze Verstecke und schwer zugängliche Orte zu deinem Vorteil.
                </li>
                <li>
                    <strong>Nutze die Umgebung:</strong> Springe und renne über Hindernisse, um anderen Spielern zu entkommen.
                </li>
                <li>
                    <strong>Koordination:</strong> Wenn du mit Freunden spielst, könnt ihr euch absprechen und Strategien entwickeln, um länger zu überleben.
                </li>
            </ul>
        </div>

        <div>
            <x-copy.ankor id="rewards">
                <h4 class="font-semibold">Belohnungen</h4>
            </x-copy.ankor>

            <p>
                Jeder Überlebende erhält am Ende des Spiels, wenn das TNT explodiert, etwas Mystery Dust als Belohnung.
            </p>
        </div>
