    <div class="flex flex-col gap-y-4">
        <div>
            <x-copy.ankor id="what-is-it">
                <h4 class="font-semibold">Was ist All Against All?</h4>
            </x-copy.ankor>

            <p>
                All Against All ist ein intensiver und kompetitiver Spielmodus, in dem alle Spieler gegeneinander antreten, um als Letzter zu überleben.
            </p>
        </div>

        <div>
            <x-copy.ankor id="how-to-play">
                <h4 class="font-semibold">Spielbeschreibung</h4>
            </x-copy.ankor>

            <ul>
                <li>
                    <strong>Keine Teams:</strong> Es gibt keine Teams; jeder Spieler kämpft alleine.
                </li>
                <li>
                    <strong>Feste Ausrüstung:</strong> Spieler spawnen mit festen Waffen und Ausrüstung.
                </li>
                <li>
                    <strong>Frei für alle:</strong> Alle Spieler sind frei, sich gegenseitig anzugreifen.
                </li>
                <li>
                    <strong>Überleben:</strong> Das Ziel ist es, so lange wie möglich zu überleben und alle anderen Spieler zu besiegen.
                </li>
                <li>
                    <strong>Keine Respawns:</strong> Es gibt keine Respawns; wenn ein Spieler besiegt ist, ist er aus dem Spiel ausgeschieden.
                </li>
            </ul>
        </div>

        <div>
            <x-copy.ankor id="maps">
                <h4 class="font-semibold">Karten</h4>
            </x-copy.ankor>

            <p>
                Wir bieten eine Vielzahl von Karten an, auf denen du All Against All spielen kannst. Jede Karte bietet ein einzigartiges Layout und verschiedene strategische Möglichkeiten.
            </p>
        </div>

        <div>
            <x-copy.ankor id="tips-and-tricks">
                <h4 class="font-semibold">Tipps und Tricks</h4>
            </x-copy.ankor>

            <ul>
                <li>
                    <strong>Sei vorsichtig:</strong> Halte dich von großen Gruppen fern und sei vorsichtig, wenn du dich anderen Spielern näherst.
                </li>
                <li>
                    <strong>Nutze die Umgebung:</strong> Lerne die Karten kennen und nutze Deckung und Verstecke zu deinem Vorteil.
                </li>
                <li>
                    <strong>Halte dich in Bewegung:</strong> Bleibe nicht an einem Ort stehen, um Angriffen auszuweichen und deine Gegner zu überraschen.
                </li>
            </ul>
        </div>
    </div>
</x-layout>
